package com.example.srsproject;

public class EmployeeModel {

    private Integer id;
    private String name;

    public EmployeeModel(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
